%include	"sconst.inc"

_NR_get_ticks		equ	0		;要与global.c中的sys_call_table对应
_NR_printx		equ	1
INT_VECTOR_SYS_CALL	equ	0x90

global	get_ticks
global	printx

bits	32
[section .text]

;=================================================================================================
;	int get_ticks();
;=================================================================================================
get_ticks:
	mov	eax, _NR_get_ticks
	int	INT_VECTOR_SYS_CALL
	ret

;=================================================================================================
;	void printx(char* s);
;=================================================================================================
printx:
	mov	eax, _NR_printx
	mov	edx, [esp + 4]
	int	INT_VECTOR_SYS_CALL
	ret
